# TypeCasting = the process of converting a variable from one data type to another
#               str(), int(), float(), bool()

name = "Anas ouamira"
age = "20"
gpa = "3.2"
is_student = True

# converting a variables:
age = int(age)
gpa = float(gpa)
print(f"my name is : {name}")
print(f"Age : {age}")
print(f"gpa : {gpa}")
print(f"Are you student : {is_student}")